package JavaOOP.WorkingWithAbstractions_3.Exercise.CardsWithPower_3;

public class Card {
    private int power;
    private String name;

    public Card(int power, String name) {
        this.power = power;
        this.name = name;
    }

    public int getPower() {
        return power;
    }

    public String getName() {
        return name;
    }
}
